<h1>Kalah dari Arab Saudi, Timnas Futsal Indonesia Gagal Lolos ke Piala Asia Futsal 2024</h1>

<img class="foto" src="https://assets.skor.id/Whats_App_Image_2023_10_10_at_18_58_57_1ad0cdf573.jpeg" alt="gambar">

<p >
Timnas futsal Indonesia harus mengubur target tampil di Piala Dunia Futsal 2024 gagal lolos ke Piala Asia Futsal 2024.
Kepastian itu terjadi setelah timnas futsal Indonesia mengalami kekalahan 2-3 dari Arab Saudi, Kamis (12/10/2023) dini hari WIB.
Hasil di Green Hall, Dammam, Arab Saudi, membuat Garuda finis di posisi tiga klasemen Grup B Kualifikasi Piala Asia Futsal 2024. 
</p>

<p>
Timnas futsal Indonesia hanya meraih empat poin dari tiga laga, kalah dari Afghanistan dan Arab Saudi dengan tujuh dan enam poinnya. 
Garuda sejatinya hanya butuh hasil imbang dalam pertandingan terakhir menghadapi tim tuan rumah, tetapi malah dibuat kesulitan. 
Mereka tertinggal tiga gol di babak pertama dan hanya bisa membalas melalui Ardiansyah Runtuboy (24'), Firman Adriansyah (33'). 
Untuk diketahui, Piala Asia Futsal 2024 juga berlaku untuk menentukan wakil-wakil Asia yang lolos ke Piala Dunia Futsal 2024.
</p>

<h2>Jalannya Pertandingan Timnas Futsal Indonesia vs Arab Saudi </h2>

<p>
Timnas futsal Indonesia langsung tampil menekan, tapi Arab Saudi tak tinggal diam dan lebih banyak mengatur tempo.
Setelah saling balas serangan tak kunjung berbuah hasil, tim tuan rumah bisa unggul lebih dulu berkat gol pada menit ke-14. 
Timnas futsal Indonesia kembali kebobolan semenit berselang melalui serangan balik lawan, ketika tampil lebih menekan. 
Timnas futsal Arab Saudi memperlebar keunggulan jadi 3-0 di menit ke-18. Gol berawal dari skema tendangan bebas.
</p>

<p>Evan Soumilena dan kolega tampil agresif sejak babak kedua dimulai, meski tak power play seraya di akhir babak pertama. 
Kebuntuan akhirnya berhasil dipecahkan. Gol balasan tercipta pada menit ke-24 melalui sepakan Ardiansyah Runtuboy.  
Permainan power play yang diterapkan timnas futsal Indonesia berbuah gol kedua via Firman Adriansyah di menit ke-33. 
Garuda harus kehilangan Samuel Eko yang diberi kartu kuning kedua saat menggagalkan serangan balik di menit ke-35. 
Timnas futsal Indonesia bisa selamat dari kebobolan dalam kondisi tampil dengan empat pemain, dan kembali menekan saat norma
Akan tetapi power play dan penampilan menekan yang ditampilkan tidak bisa berbuah gol tambahan dan akhirnya kalah 2-3. </p>
